        $this->Cell(0, 5, clean_pdf_text('Page ' . $this->PageNo() . '/{nb} - Generated by ' . $this->site_name), 0, 0, 'C');
    }

    // NbLines helper for MultiCell height calculation
    function NbLines($w, $txt) {
        $cw = &$this->CurrentFont['cw'];
        if($w==0) $w = $this->w - $this->rMargin - $this->x;
        $wmax = max(1, ($w-2*$this->cMargin)*1000/$this->FontSize);
        $s = str_replace("\r", '', $txt);
        $nb = strlen($s);
        if($nb>0 && $s[$nb-1]=="\n") $nb--;
        $sep = -1; $i = 0; $j = 0; $l = 0; $nl = 1;
        while($i<$nb) {
            $c = $s[$i];
            if($c=="\n") { $i++; $sep = -1; $j = $i; $l = 0; $nl++; continue; }
            if($c==' ') $sep = $i;
            $l += (isset($cw[$c]) ? $cw[$c] : 0);
            if($l>$wmax) {
                if($sep==-1) { if($i==$j) $i++; }
                else $i = $sep+1;
                $sep = -1; $j = $i; $l = 0; $nl++;
            } else $i++;
        }
        return $nl;
    }
}

