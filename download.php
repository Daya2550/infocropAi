// Helper: clean text for PDF
function clean_pdf_text($text) {
    if (!is_string($text)) $text = (string)($text ?? '');
    if ($text === '') return '';

    // Preserve meaningful symbols before stripping
    $text = str_replace(['₹', 'â‚¹'], 'Rs.', $text);
    $text = str_replace(['★', '☆', '⭐'], '*', $text);
    $text = str_replace(['✔', '✓'], '[OK]', $text);
    $text = str_replace(['✖', '✗', '✘'], '[X]', $text);
    $text = str_replace(['→', '➔', '➜', '➡'], '->', $text);
    $text = str_replace(['←'], '<-', $text);
    $text = str_replace(['↑'], '^', $text);
    $text = str_replace(['↓'], 'v', $text);
    $text = str_replace(['•', '●', '◉'], chr(149), $text);
    $text = str_replace(['—', '–'], '-', $text);
    $text = str_replace(["\u{2018}", "\u{2019}", "\u{201A}"], "'", $text);
    $text = str_replace(["\u{201C}", "\u{201D}", "\u{201E}"], '"', $text);
    $text = str_replace("\u{2026}", '...', $text);

    // Strip ALL emoji & symbol Unicode blocks comprehensively
    $text = (string) preg_replace('/[\x{1F000}-\x{1FFFF}]/u', '', $text);  // All SMP emojis
    $text = (string) preg_replace('/[\x{2600}-\x{27BF}]/u', '', $text);    // Misc symbols, Dingbats
    $text = (string) preg_replace('/[\x{FE00}-\x{FE0F}]/u', '', $text);    // Variation selectors
    $text = (string) preg_replace('/[\x{200D}]/u', '', $text);             // Zero-width joiner
    $text = (string) preg_replace('/[\x{20E3}]/u', '', $text);             // Combining enclosing keycap
    $text = (string) preg_replace('/[\x{E0020}-\x{E007F}]/u', '', $text);  // Tags block
    $text = (string) preg_replace('/[\x{2300}-\x{23FF}]/u', '', $text);    // Misc technical
    $text = (string) preg_replace('/[\x{2B00}-\x{2BFF}]/u', '', $text);    // Misc symbols & arrows
    $text = (string) preg_replace('/[\x{3000}-\x{303F}]/u', '', $text);    // CJK symbols
    $text = (string) preg_replace('/[\x{25A0}-\x{25FF}]/u', '', $text);    // Geometric shapes
    $text = (string) preg_replace('/[\x{2190}-\x{21FF}]/u', '', $text);    // Arrows

    // Convert to Latin-1 (FPDF encoding)
    if (function_exists('iconv')) {
        $text = iconv('UTF-8', 'ISO-8859-1//TRANSLIT//IGNORE', $text) ?: $text;
    } else {
        $text = utf8_decode($text);
    }

    // Final safety: strip any remaining non-printable/non-Latin-1 characters
    $text = preg_replace('/[^\x20-\x7E\xA0-\xFF\n\r\t]/', '', $text);

    return trim($text);
}

// Helper: render table in PDF (premium layout)
function renderPDFTable($pdf, $data) {
    if (empty($data)) return;

    // Disable FPDF's auto page break – we manage breaks ourselves.
    // Without this, MultiCell fires its OWN AddPage() mid-row, producing blank pages.
    $pdf->SetAutoPageBreak(false);

    // ── Filter out pure separator / empty rows ──────────────────
    $filtered = [];
    $maxCols = 0;
    foreach ($data as $row) {
        $joined = implode('', array_map('trim', $row));
        if ($joined === '' || preg_match('/^[-:|\\s]+$/', $joined)) continue;
        $cleanedRow = array_map('trim', $row);
        $maxCols = max($maxCols, count($cleanedRow));
        $filtered[] = $cleanedRow;
    }
    if (empty($filtered)) { $pdf->SetAutoPageBreak(true, 10); return; }

    // Equalize columns (pad with empty string)
    foreach ($filtered as &$row) {
        while (count($row) < $maxCols) {
            $row[] = '';
        }
    }
    unset($row);

    $cols   = $maxCols;
    $total  = 190; // usable page width in mm

    // ── Smart column widths ─────────────────────────────────────
    if ($cols >= 3) {
        $firstW = $total * 0.20;
        $restW  = ($total - $firstW) / ($cols - 1);
        $widths = array_merge([$firstW], array_fill(0, $cols - 1, $restW));
    } else {
        $w = $total / $cols;
        $widths = array_fill(0, $cols, $w);
    }

    // ── Colours ─────────────────────────────────────────────────
    $hdrBg   = [27,  94,  32];
    $hdrFg   = [255, 255, 255];
    $rowEven = [245, 250, 245];
    $rowOdd  = [255, 255, 255];
    $rowFg   = [40,  40,  40];
    $borderR = 200; $borderG = 220; $borderB = 200;
    $boldBg  = [232, 245, 233];

    $lineH = 5;
    $padX  = 1.5;

    // ── Helpers ─────────────────────────────────────────────────
    $stripBold = fn($t) => trim(preg_replace('/\*{1,2}(.*?)\*{1,2}/u', '$1', $t));
    $rowIsBold = function($row) {
        foreach ($row as $cell) {
            if (preg_match('/\*\*.*?\*\*/', $cell)) return true;
        }
        return false;
    };

    // ── Inline helper: draw a single header row at current Y ────
    $drawHeader = function() use ($pdf, $filtered, $widths, $hdrBg, $hdrFg, $borderR, $borderG, $borderB, $stripBold) {
        $pdf->SetFont('Arial', 'B', 8.5);
        $pdf->SetTextColor(...$hdrFg);
        $pdf->SetFillColor(...$hdrBg);
        $pdf->SetDrawColor($borderR, $borderG, $borderB);
        $pdf->SetLineWidth(0.3);
        $x0 = $pdf->GetX(); $y0 = $pdf->GetY();
        foreach ($filtered[0] as $j => $cell) {
            $pdf->SetXY($x0 + array_sum(array_slice($widths, 0, $j)), $y0);
            $pdf->Cell($widths[$j], 9, clean_pdf_text($stripBold($cell)), 1, 0, 'C', true);
        }
        $pdf->Ln(9);
    };

    // Draw the first header
    $drawHeader();

    // ── Data rows ────────────────────────────────────────────────
    $pdf->SetFont('Arial', '', 8);
    $pdf->SetLineWidth(0.2);

    for ($i = 1; $i < count($filtered); $i++) {
        $row    = $filtered[$i];
        $isBold = $rowIsBold($row);

        // Calculate row height
        $maxLines = 1;
        foreach ($row as $j => $cell) {
            $txt      = clean_pdf_text($stripBold($cell));
            if (method_exists($pdf, 'NbLines')) {
                $lines = $pdf->NbLines(max(1, $widths[$j] - $padX * 2), $txt);
            } else {
                $cpl      = max(1, (int)(($widths[$j] - $padX * 2) / 2.0));
                $lines    = max(1, (int)ceil(strlen($txt) / $cpl));
            }
            $maxLines = max($maxLines, $lines);
        }
        $rowH = max(7, $maxLines * $lineH + 2);

        // Manual page break
        if ($pdf->GetY() + $rowH > 252) {  // 252 = 277mm page bottom minus 25mm footer margin
            $pdf->AddPage();
            $drawHeader();
            $pdf->SetFont('Arial', '', 8);
            $pdf->SetLineWidth(0.2);
        }

        // Row background & font
        if ($isBold) {
            $pdf->SetFillColor(...$boldBg);
            $pdf->SetFont('Arial', 'B', 8);
        } elseif ($i % 2 === 0) {
            $pdf->SetFillColor(...$rowEven);
        } else {
            $pdf->SetFillColor(...$rowOdd);
        }
        $pdf->SetTextColor(...$rowFg);   // ← always reset to dark BEFORE drawing
        $pdf->SetDrawColor($borderR, $borderG, $borderB);

        $x = $pdf->GetX();
        $y = $pdf->GetY();

        foreach ($row as $j => $cell) {
            $txt = clean_pdf_text($stripBold($cell));
            $cx  = $x + array_sum(array_slice($widths, 0, $j));
            $pdf->Rect($cx, $y, $widths[$j], $rowH, 'FD');
            $pdf->SetXY($cx + $padX, $y + 1);
            $pdf->MultiCell($widths[$j] - $padX * 2, $lineH, $txt, 0, 'L');
        }
        $pdf->SetXY($x, $y + $rowH);

        if ($isBold) $pdf->SetFont('Arial', '', 8);
    }

    // Bottom border
    $pdf->SetDrawColor($borderR, $borderG, $borderB);
    $pdf->SetLineWidth(0.3);
    $pdf->Line($pdf->GetX(), $pdf->GetY(), $pdf->GetX() + $total, $pdf->GetY());
    $pdf->Ln(4);

    // Restore auto page break for the rest of the document (25mm clears 20mm footer)
    $pdf->SetAutoPageBreak(true, 25);
}


